fruit=input("What fruit do you want to sort?")
if fruit.lower()=="apples":
    print("Apples go in bin num. 1")
elif fruit.lower()=='oranges':
    print("Oranges go in bin 2")
elif fruit.lower()=="bananas":
    print("Bananas go in bin 3")
else:
    print('Error! I do not recognize this fruit!')

    
